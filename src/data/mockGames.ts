const now = new Date();

function createRelativeDate(
  dayOffset: number,
  hour: number,
  minute: number,
): string {
  const date = new Date(now);
  date.setDate(now.getDate() + dayOffset);
  date.setHours(hour, minute, 0, 0);
  return date.toISOString();
}

